#include<stdio.h>
void main()
{
	// >,<,>=,<=,==,!= --> return value --> 0 or 1-->write the condition
	int a = 45, b = 56;
	printf("a>b --> %d\n",a>b);//0
	printf("a<b -->%d\n",a<b);//1
	printf("a>=b -->%d\n",a>=b);//0 
	printf("a<=b -->%d\n",a<=b);//1
	printf("a==b -->%d\n",a==b);//0
	printf("a!=b -->%d\n",a!=b);//1
}
